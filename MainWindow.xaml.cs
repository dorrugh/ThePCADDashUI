    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }
        //opens the launcher window and closes the UI window
        private void Launcher_Click(object sender, RoutedEventArgs e)
        {
            GameLauncher Launcher = new GameLauncher();
            Launcher.Show();
            this.Close();
        }
        //event handler for my custom close button in corner of UI Window
        private void CloseApplication1(object sender, MouseButtonEventArgs e)
        {
            try
            {
                Close();
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message);
            }
        }
        //event handler for my custom close button in corner of UI Window
        private void MinimizaApplication1(object sender, MouseButtonEventArgs e)
        {
            try
            {
                this.WindowState = WindowState.Minimized;
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message);
            }
        }


    }
